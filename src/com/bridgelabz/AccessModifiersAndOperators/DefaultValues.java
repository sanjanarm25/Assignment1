package com.bridgelabz.AccessModifiersAndOperators;

public class DefaultValues {
    static int value1;
    static long value2;
    static double value3;
    static short value4;
    static byte value5;
    static float value6;
    static char value7;
    static boolean value8;
    public static void main(String[] args) {

        System.out.println("int default value is : " +value1);
        System.out.println("long default value is : " +value2);
        System.out.println("double default value is : " +value3);
        System.out.println("short default value is : " +value4);
        System.out.println("byte default value is : " +value5);
        System.out.println("float default value is : " +value6);
        System.out.println("char default value is : " +value7);
        System.out.println("boolean default value is : " +value8);

    }
}
